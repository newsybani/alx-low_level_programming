#include "main.h"
/**
 * print_diagonal - prints a diagonal line with some bumps
 * @n: number of times the diagonal line is printed
 * 
 * Return: nothing
 */
void print_diagonal(int n)
{
	int i;
	int j;

	for (i = 0; i < n; i++)
	{
		for (j = 0; j < i; j++)
		{
			_putchar(' ');
		}
		_putchar('\\');
		_putchar('\n');
	}

	if (n <= 0)
	{
		_putchar('\n');
	}
}
